my_list = ['a','b','c','d','e','f','g','h','i']
print(my_list[2:5]) #Should give you ['c', 'd', 'e']

print(my_list[:-5]) #Should give you ['a', 'b', 'c', 'd']

print(my_list[5:]) #Should give you ['f', 'g', 'h', 'i']

print(my_list[:]) #Should give you ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i']
